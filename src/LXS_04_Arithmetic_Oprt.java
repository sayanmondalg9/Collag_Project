public class LXS_04_Arithmetic_Oprt {
  public static void main(String[] args) {
    int a = 5, b = 6;
    System.out.println("Addition: num1 + num2 = " + (a + b));
    System.out.println("Subtraction: num1 - num2 = " + (a - b));
    System.out.println("Multiplication: num1 x num2 = " + (a * b));
    System.out.println("Divition: num1 / num2 = " + ((float) a / b));
    System.out.println("Modulus: num1 % num2 = " + (a % b));
  }
}
